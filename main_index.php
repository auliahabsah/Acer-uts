<h2>Welcome To Gallery Photography</h2>
<div style="text-align: justify;margin-top: 25px"><p>Fotografi dimulai pada abad ke-19. Tahun 1893 merupakan tahun awal kelahiran fotografi. Pada saat itu, di Perancis dinyatakan secara resmi bahwa fotografi adalah sebuah terobosan teknologi. saat itu, rekaman dua dimensi seperti yang dilihat mata sudah bisa dibuat permanen. Fotografi kian populer seiring dengan perkembangan teknologi. kata fotografi berasal dari dua kata Yunani kuno, yaitu photo yang artinya cahaya dan graphos yang artinya untuk menggambar. Dengan begitu, secara harfiah bisa diartikan sebagai menggambar dengan cahaya.</p></div>
